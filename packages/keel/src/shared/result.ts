/**
 * Result convention — Architecture v1.0, Doc 03 §3.11.
 *
 * For expected, recoverable outcomes where throwing would conflate control
 * flow with failure. Errors that cross module boundaries still use the
 * KeelError hierarchy (C58); Result carries them, it does not replace them.
 */

export interface Ok<T> {
  readonly ok: true;
  readonly value: T;
}

export interface Err<E> {
  readonly ok: false;
  readonly error: E;
}

export type Result<T, E> = Ok<T> | Err<E>;

export function ok<T>(value: T): Ok<T> {
  return { ok: true, value };
}

export function err<E>(error: E): Err<E> {
  return { ok: false, error };
}

export function isOk<T, E>(result: Result<T, E>): result is Ok<T> {
  return result.ok;
}

export function isErr<T, E>(result: Result<T, E>): result is Err<E> {
  return !result.ok;
}
